		<h2>Que es GIT</h2>
		<p>
			Git es un software de control de versiones diseñado por Linus Torvalds, pensando en la eficiencia, la confiabilidad y compatibilidad del mantenimiento de versiones de aplicaciones cuando estas tienen un gran número de archivos de código fuente.
		</p>
		<br>
		<h2>Comandos basicos de Git</h2>
		<ol>
			<li>git init --> inicia un nuevo repositorio de git</li>
			<li>git status --> Nos deja ver el estatus del proyecto</li>
			<li>git add "file name" --> Nos permite añadir un archivo a nuetro repositorio</li>
			<li>git add -A --> Nos Permite añadir todos los archivos a nuestro repositorio</li>
			<li>git commit -m "desc cambio" --> Nos permite añadir un nuevo cambio al repositorio</li>
			<li>git log --> Nos permite ver todos los commit del repositorio</li>
			<li>git checkout --> Nos permite regrfesar a cualquier commit del repositorio</li>
			<li>git checkout master --> Nos permite regresar al ultimo commit</li>
			<li>git reset --soft --> Nos permite borrar commits sin consecuencia</li>
			<li>git reset --hard --> Nos permite borrar commits con consecuencias(borra todos los cambios realizados)</li>
		</ol>
